    private void buttonCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCheckActionPerformed
        // TODO add your handling code here:
        String acc=textFieldAcc.getText();
        if(!Pattern.matches(ATM.regexAcc, acc)){
            JOptionPane.showMessageDialog(buttonCheck, "Please enter a valid account number", "", JOptionPane.ERROR_MESSAGE);
            return;
        }try{
            Connection con=DriverManager.getConnection(ATM.url);
            Statement statement=con.createStatement();
            String sql="select balance from accounts where acc_no="+acc;
            ResultSet res=statement.executeQuery(sql);
            if(!res.next()){
                JOptionPane.showMessageDialog(buttonCheck, "Not found", "", JOptionPane.ERROR_MESSAGE);
            return;
            }
            String balance=Integer.toString(res.getInt(1));
            JOptionPane.showMessageDialog(buttonCheck, "Balance = â‚¹"+balance, "", JOptionPane.INFORMATION_MESSAGE);
            ATM.panelSwap(this, new Home());
        }catch(Exception e) {e.printStackTrace();}
        
    }//GEN-LAST:event_buttonCheckActionPerformed


